// Цикл for
let number = 5;

for(let i=1; i<=10; i++) {
    let multiplication = number * i;
    console.log(`${number} * ${i} = ${multiplication}`)
}

// Цикл while

let b = 1;

while(b <= 10) {
    let multiplication2 = number * b;
    console.log(`${number} * ${b} = ${multiplication2}`);
    b++;
}